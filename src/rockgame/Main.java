    private void ExitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ExitMouseClicked
        // TODO add your handling code here:
        if(playerScore == cPUScore) {
            JOptionPane.showMessageDialog(this, "It's a Tie!","RESULT",JOptionPane.INFORMATION_MESSAGE);
        }
        else if(playerScore > cPUScore) {
             JOptionPane.showMessageDialog(this, "You Won!","RESULT",JOptionPane.INFORMATION_MESSAGE);
        }
        else {
                    JOptionPane.showMessageDialog(this, "Alas! You Lost","RESULT",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_ExitMouseClicked

