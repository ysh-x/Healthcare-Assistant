    void Judge(String Player, String CPU) {
        
        if(Player.equals("ROCK") && CPU.equals("ROCK")) {
            Result.setText("TIE");
        }
        else if(Player.equals("PAPER") && CPU.equals("PAPER")) {
            Result.setText("TIE");
        }
        else if(Player.equals("SCISSORS") && CPU.equals("SCISSORS")) {
            Result.setText("TIE");
        }
        else if(Player.equals("ROCK") && CPU.equals("SCISSORS")) {
            Result.setText("PLAYER");
            playerScore++;
            PlayerScore.setText(Integer.toString(playerScore));
            
            
        }
         else if(Player.equals("SCISSORS") && CPU.equals("PAPER")) {
            Result.setText("PLAYER");
            playerScore++;
            PlayerScore.setText(Integer.toString(playerScore));
            
            
        }
         else if(Player.equals("PAPER") && CPU.equals("ROCK")) {
            Result.setText("PLAYER");
            playerScore++;
            PlayerScore.setText(Integer.toString(playerScore));
            
            
        }
         else if(Player.equals("SCISSORS") && CPU.equals("ROCK")) {
            Result.setText("CPU");
            cPUScore++;
            CPUScore.setText(Integer.toString(cPUScore));
            
            
        }
          else if(Player.equals("PAPER") && CPU.equals("SCISSORS")) {
            Result.setText("CPU");
            cPUScore++;
            CPUScore.setText(Integer.toString(cPUScore));
            
            
        }
          else if(Player.equals("ROCK") && CPU.equals("PAPER")) {
            Result.setText("CPU");
            cPUScore++;
            CPUScore.setText(Integer.toString(cPUScore));
            
            
        }
        
        
    }
   
    
